    private void GenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GenerarActionPerformed
        int contador = 0;
        Object[][] data = new Object[factura_acumulador.length][factura_acumulador[0].length];
        for (int i = 0; i < factura_acumulador.length; i++) {
            for (int j = 0; j < factura_acumulador[i].length; j++) {
                data[i][j] = factura_acumulador[i][j];
            }
        }
        String[] titulos = {"N째 venta", "C처digo", "Unidades", "Ganancia"};
        modelo = new DefaultTableModel(data,titulos);
        Registro_Ventas.setModel(modelo);
        
        for (int i = 0; i < factura_acumulador.length; i++) {
            if(factura_acumulador[i][0] != 0){
                contador += 1;
            }
        }
        Total_ventas.setText(String.valueOf(contador));
    }//GEN-LAST:event_GenerarActionPerformed

